  use fruit
  use iso_c_binding
  use vector_int_mod
  implicit none
  logical ok

  call init_fruit

  call test_vector_int

  call fruit_summary
  call fruit_finalize

  call is_all_successful(ok)
  if (.not. ok) then
     call exit(1)
  endif

contains

  subroutine test_vector_int

    type(vector_int) v1
    integer(C_INT), pointer :: ivalue

    v1 = vector_int_ctor()

    call v1%push_back(1)

    ivalue => v1%at(0_C_SIZE_T)
    call assert_equals(1, ivalue)

